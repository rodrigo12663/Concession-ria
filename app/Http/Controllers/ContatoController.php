    {
       

            $contato = new \App\Contato();
            $contato->name=$request->nome;
            $contato->email=$request->email;
            $contato->telefone=$request->telefone;
            $contato->mensagem=$request->msg;
          
            $contato->save();
            flash("Obrigado pelo seu Feedback")->success();
            return view('contato');
    }

